import { chromium } from 'playwright';
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 1280, height: 800 } });
const page = await ctx.newPage();
const errors = [];
page.on('console', m => { if (m.type() === 'error') errors.push('CONSOLE: ' + m.text()); });
page.on('pageerror', e => errors.push('PAGEERR: ' + e.message));
await page.goto('http://localhost:5173/', { waitUntil: 'networkidle', timeout: 25000 });
await page.waitForTimeout(1500);
const pieces1 = await page.locator('.square .piece').count();
console.log('Initial pieces count:', pieces1);
await page.selectOption('#piece-style-select', 'bold');
await page.waitForTimeout(800);
const pieces2 = await page.locator('.square .piece').count();
console.log('After bold:', pieces2);
await page.screenshot({ path: '/tmp/ajedrez-v121-2d-bold.png' });
await page.selectOption('#piece-style-select', 'outline');
await page.waitForTimeout(800);
const pieces3 = await page.locator('.square .piece').count();
console.log('After outline:', pieces3);
await page.screenshot({ path: '/tmp/ajedrez-v121-2d-outline.png' });
await page.selectOption('#piece-style-select', 'filled');
await page.waitForTimeout(800);
const pieces4 = await page.locator('.square .piece').count();
console.log('After filled:', pieces4);
await page.screenshot({ path: '/tmp/ajedrez-v121-2d-filled.png' });
await page.selectOption('#piece-style-select', 'minimal');
await page.waitForTimeout(800);
const pieces5 = await page.locator('.square .piece').count();
console.log('After minimal:', pieces5);
await page.screenshot({ path: '/tmp/ajedrez-v121-2d-minimal.png' });
await page.selectOption('#render-mode-select', '3d');
await page.waitForTimeout(3500);
await page.selectOption('#piece-style-select', 'ornate');
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/ajedrez-v121-3d-ornate.png' });
await page.selectOption('#piece-style-select', 'classic');
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/ajedrez-v121-3d-classic.png' });
await page.selectOption('#piece-style-select', 'bold');
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/ajedrez-v121-3d-bold.png' });
console.log('ERR:', errors.length ? errors.join('\n  ') : '(none)');
await browser.close();
